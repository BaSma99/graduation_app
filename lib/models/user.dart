import 'package:flutter/material.dart';

class User {
  final firstName;
  final lastName;
  final userName;
  final email;
  final password;
  final confirmPassword;
  final birthDate;
  final field;
  final city;
  final sills;
  final graduated;

  User(
    this.firstName,
    this.lastName,
    this.userName,
    this.email,
    this.password,
    this.confirmPassword,
    this.birthDate,
    this.field,
    this.city,
    this.sills,
    this.graduated,
  );
}
